package basics;

public class Mul{
	void product() {
		System.out.println("no data");
	}
	void product(int a,int b) {
		System.out.println(a*b);
	}
	void product(int a,int b,int c) {
		System.out.println(a*b*c);

	}
	void product(double a,double b) {
		System.out.println(a*b);

	}


}
